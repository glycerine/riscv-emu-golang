//go:build arm64

// ops_arm64.s - Float arithmetic with flag clear + immediate FPSR capture.
//
// ARM64 FPSR cumulative exception bits:
//   bit4 IXC(inexact)   -> rv NX bit0
//   bit3 UFC(underflow) -> rv UF bit1
//   bit2 OFC(overflow)  -> rv OF bit2
//   bit1 DZC(divzero)   -> rv DZ bit3
//   bit0 IOC(invalid)   -> rv NV bit4

#include "textflag.h"

#define CLEAR_FLAGS \
	MOVD	ZR, R0 \
	MSR	R0, FPSR

#define CAPTURE_FLAGS \
	MRS	FPSR, R0 \
	MOVD	ZR, R1 \
	MSR	R1, FPSR \
	LSR	$4, R0, R1 \
	AND	$1, R1, R1 \
	LSR	$2, R0, R2 \
	AND	$2, R2, R2 \
	ORR	R2, R1, R1 \
	AND	$4, R0, R2 \
	ORR	R2, R1, R1 \
	LSL	$2, R0, R2 \
	AND	$8, R2, R2 \
	ORR	R2, R1, R1 \
	LSL	$4, R0, R2 \
	AND	$16, R2, R2 \
	ORR	R2, R1, R1

TEXT ·FFlags(SB),NOSPLIT|NOFRAME,$0-4
	CAPTURE_FLAGS
	MOVW	R1, ret+0(FP)
	RET

TEXT ·ClearFFlags(SB),NOSPLIT|NOFRAME,$0-0
	CLEAR_FLAGS
	RET

TEXT ·RawMXCSR(SB),NOSPLIT|NOFRAME,$0-4
	MRS	FPSR, R0
	MOVW	R0, ret+0(FP)
	RET

TEXT ·AddF32(SB),NOSPLIT|NOFRAME,$0-16
	CLEAR_FLAGS
	FMOVS	a+0(FP), F0
	FMOVS	b+4(FP), F1
	FADDS	F1, F0, F0
	CAPTURE_FLAGS
	FMOVS	F0, ret+8(FP)
	MOVW	R1, flags+12(FP)
	RET

